#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	size_t	x;

	x = 0;
	if (!s || !f)
		return ;
	while (*s)
	{
		f(x, s);
		s++;
		x++;
	}
}
